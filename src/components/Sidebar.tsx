export default function Sidebar({ currentView, onViewChange }: SidebarProps) {
  const selectedCellId = useAppStore((state) => state.selectedCellId);
  const selectedCell = useAppStore((state) => state.getCellById(selectedCellId || ''));
  const { can } = usePermission();

  const groupedItems = useMemo(() => {
    const filteredItems = menuItems.filter((item) => {
      if (!item.permission) return true;
      return can(item.permission as any);
    });

    const groups: { name: string | null; items: typeof filteredItems }[] = [];
    let currentGroup: string | null = null;
    for (const item of filteredItems) {
      const groupName = item.group || null;
      if (groupName !== currentGroup) {
        currentGroup = groupName;
        groups.push({ name: groupName, items: [] });
      }
      groups[groups.length - 1].items.push(item);
    }
    return groups;
  }, [can]);

  return (
    <aside className="w-56 bg-white border-r border-gray-200 flex flex-col">
      <nav className="flex-1 py-4 overflow-y-auto">
        {groupedItems.map((group, gi) => (
          <div key={gi} className="mb-2">
            {group.name && (
              <div className="px-6 pt-3 pb-1 text-xs font-semibold text-gray-400 uppercase tracking-wider">
                {group.name}
              </div>
            )}
            <ul className="space-y-1 px-3">
              {group.items.map((item) => (
                <li key={item.id}>
                  <button
                    onClick={() => onViewChange(item.id)}
                    className={`w-full flex items-center gap-3 px-3 py-2 text-sm rounded-lg transition-colors ${
                      currentView === item.id
                        ? 'bg-earth-100 text-earth-700 font-medium'
                        : 'text-gray-600 hover:bg-gray-100'
                    }`}
                  >
                    {item.icon}
                    {item.label}
                  </button>
                </li>
              ))}
            </ul>
          </div>
        ))}
      </nav>
      {selectedCell && (
        <div className="p-3 border-t border-gray-200">
          <div className="text-xs text-gray-500 mb-1">当前方格</div>
          <div className="text-sm font-medium text-gray-800">{selectedCell.code}</div>
          <div className="text-xs text-gray-500 mt-1">
            行{selectedCell.row} 列{selectedCell.col}
          </div>
        </div>
      )}
    </aside>
  );
}
